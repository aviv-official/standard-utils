class Timeline {
  constructor(config) {
    defineProperty(this, "_bzz", void 0);

    defineProperty(this, "_decode", void 0);

    defineProperty(this, "_encode", void 0);

    defineProperty(this, "_feed", void 0);

    defineProperty(this, "_signParams", void 0);

    this._bzz = config.bzz;
    this._decode = config.decode || defaultDecode;
    this._encode = config.encode || defaultEncode;
    this._feed = config.feed;
    this._signParams = config.signParams;
  }

  async getChapter(id, options = {}) {
    const res = await this._bzz.download(id, { ...options,
      mode: 'raw'
    });
    const chapter = await this._decode(res);
    chapter.id = id;
    return chapter;
  }

  async postChapter(chapter, options = {}) {
    const encoded = await this._encode(chapter);
    return await this._bzz.uploadFile(encoded, options);
  }

  async getLatestChapterID(options = {}) {
    try {
      return await this._bzz.getFeedContentHash(this._feed, options);
    } catch (err) {
      if (err.status === 404) {
        return null;
      } else {
        throw err;
      }
    }
  }

  async getLatestChapter(options = {}) {
    const id = await this.getLatestChapterID(options);

    if (id == null) {
      return null;
    }

    return await this.getChapter(id, options);
  }

  async setLatestChapterID(chapterID, options) {
    await this._bzz.setFeedContentHash(this._feed, chapterID, options, this._signParams);
  }

  async setLatestChapter(chapter, options = {}) {
    const [chapterID, feedMeta] = await Promise.all([this.postChapter(chapter, options), this._bzz.getFeedMetadata(this._feed)]);
    await this._bzz.postFeedChunk(feedMeta, `0x${chapterID}`, {
      headers: options.headers,
      timeout: options.timeout
    }, this._signParams);
    return chapterID;
  }

  async addChapter(chapter, options = {}) {
    if (typeof chapter.previous === 'undefined') {
      chapter.previous = await this.getLatestChapterID(options);
    }

    const id = await this.setLatestChapter(chapter);
    return { ...chapter,
      id
    };
  }

  createAddChapter(chapterDefaults = {}, options = {}) {
    let previous = null;
    let previousPromise = this.getLatestChapterID(options);
    const create = createChapterFactory(chapterDefaults);
    return async partialChapter => {
      if (previous === null && previousPromise !== null) {
        previous = await previousPromise;
        previousPromise = null;
      }

      const chapter = create({ ...partialChapter,
        previous
      });
      previous = await this.setLatestChapter(chapter, options);
      return { ...chapter,
        id: previous
      };
    };
  }

  createIterator(initialID, options = {}) {
    let unitialLoaded = false;
    let nextID = initialID; // $FlowFixMe: AsyncIterator

    return {
      // $FlowFixMe: Flow doesn't support symbols as Object keys
      [Symbol.asyncIterator]() {
        return this;
      },

      next: async () => {
        if (initialID == null && !unitialLoaded) {
          nextID = await this.getLatestChapterID(options);
          unitialLoaded = true;
        }

        if (nextID == null) {
          return {
            done: true,
            value: undefined
          };
        }

        const chapter = await this.getChapter(nextID, options);
        nextID = chapter.previous;
        return {
          done: false,
          value: chapter
        };
      }
    };
  }

  createLoader(newestID, // inclusive
  oldestID, // exclusive
  options = {}) {
    return Observable.create(async subscriber => {
      try {
        for await (const chapter of this.createIterator(newestID, options)) {
          subscriber.next(chapter);

          if (chapter.previous === oldestID) {
            subscriber.complete();
            break;
          }
        }
      } catch (err) {
        subscriber.error(err);
      }
    });
  }

  async getChapters(newestID, // inclusive
  oldestID, // exclusive
  options = {}) {
    const chapters = [];

    for await (const chapter of this.createIterator(newestID, options)) {
      chapters.push(chapter);

      if (chapter.previous === oldestID) {
        break;
      }
    }

    return chapters;
  }

  pollLatestChapter(options) {
    const downloadOptions = {
      headers: options.headers,
      mode: 'raw'
    };
    return this._bzz.pollFeedContentHash(this._feed, {
      whenEmpty: 'ignore',
      changedOnly: true,
      ...options
    }).pipe(mergeMap(id => {
      return id === null ? [] : this.getChapter(id, downloadOptions);
    }));
  }

  live(options) {
    let minTimestamp = options.timestamp || Date.now();
    let previousID = options.previous || null;
    return this.pollLatestChapter(options).pipe(mergeMap(async chapter => {
      let chapters;

      if (chapter.timestamp < minTimestamp && previousID === null) {
        // Ignore chapter older than minimum timestamp
        return [];
      }

      if (previousID === null || chapter.previous == null || chapter.previous === previousID) {
        // Single chapter to push
        chapters = [chapter];
      } else {
        // There has been more than one update during the polling interval
        const slice = await this.getChapters(chapter.previous, previousID, options);
        chapters = slice.reverse().concat(chapter);
      }

      minTimestamp = chapter.timestamp;
      previousID = chapter.id;
      return chapters;
    }));
  }

}
